# if-else
hungry = False
if hungry:
    print('FEED ME!')
else:
    print("I'm not hungry")
# multi statements (elif)
loc = 'Bank'
if loc == 'Auto Shop':
    print('Cars are cool!')
elif loc == 'Bank':
    print('Money is cool!')
elif loc == 'Store':
    print('Welcome to the Store!')
else:
    print('I do not know much.')

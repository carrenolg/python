# Indexing and Slicing
letters = 'abcdefghij'
# first item
print(letters[0])
# last item
print(letters[-1], letters[len(letters)-1])
# slice: first three elements
print(letters[0:3])
# slice: last three elements
print(letters[-3:])
# slice: middle three elements
print(letters[3:6])
# slice: reverse (step:-1)
print(letters[::-1])
